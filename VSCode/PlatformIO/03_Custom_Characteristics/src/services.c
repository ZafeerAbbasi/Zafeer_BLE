tBleStatus service_AddService( void )
{
    tBleStatus ret;
    Service_UUID_t serviceUUID;
    memcpy( serviceUUID.Service_UUID_128, SERVICE_UUID , sizeof( SERVICE_UUID ) );

    /*Add Service*/
    ret = aci_gatt_add_service(
        UUID_TYPE_128,
        &serviceUUID,
        PRIMARY_SERVICE,
        7,
        &customServiceHdl
    );

    return ret;
}